/**
 * list_length - it determines the length of the linked list
 * @h: the pointer to the first node
 *
 * Return: the size of list
 */
size_t list_length(const list_t *h)
{
	size_t i = 0;

	while (h)
	{
		h = h->next;
		i++;
	}
	return (i);
}

/**
 * list_to_str - it returns an array of strings of the list->str
 * @head: the pointer to the first node
 *
 * Return: the array of strings
 */
char **list_to_str(list_t *head)
{
	list_t *node = head;
	size_t i = list_length(head), j;
	char **strs;
	char *str;

	if (!head || !i)
		return (NULL);
	strs = malloc(sizeof(char *) * (i + 1));
	if (!strs)
		return (NULL);
	for (i = 0; node; node = node->next, i++)
	{
		str = malloc(_strlen(node->str) + 1);
		if (!str)
		{
			for (j = 0; j < i; j++)
				free(strs[j]);
			free(strs);
			return (NULL);
		}

		str = _strcpy(str, node->str);
		strs[i] = str;
	}
	strs[i] = NULL;
	return (strs);
}


/**
 * print_lists - it prints all the elements of a list_t linked list
 * @h: the pointer to the first node
 *
 * Return: the size of list
 */
size_t print_lists(const list_t *h)
{
	size_t i = 0;

	while (h)
	{
		_puts(convert_number(h->num, 10, 0));
		_putchar(':');
		_putchar(' ');
		_puts(h->str ? h->str : "(nil)");
		_puts("\n");
		h = h->next;
		i++;
	}
	return (i);
}

/**
 * node_start_with - it returns the node whose string starts with a prefix
 * @node: the pointer to list head
 * @prefix: it is the string to match
 * @c: it is the next character after the prefix to match
 *
 * Return: the match node or null
 */
list_t *node_start_with(list_t *node, char *prefix, char c)
{
	char *p = NULL;

	while (node)
	{
		p = start_with(node->str, prefix);
		if (p && ((c == -1) || (*p == c)))
			return (node);
		node = node->next;
	}
	return (NULL);
}

/**
 * get_node_index - it gets the index of a node
 * @head: the pointer to the list head
 * @node: the pointer to the node
 *
 * Return: index of the node or -1
 */
ssize_t get_node_index(list_t *head, list_t *node)
{
	size_t i = 0;

	while (head)
	{
		if (head == node)
			return (i);
		head = head->next;
		i++;
	}
	return (-1);
}
